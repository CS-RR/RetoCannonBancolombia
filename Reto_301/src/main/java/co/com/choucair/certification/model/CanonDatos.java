package co.com.choucair.certification.model;

public class CanonDatos {

    private String valor;
    private String plazo;
    private String porcentaje;
    private String modalidad;
    private String valormin;
    private String valormax;

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public String getPlazo() {
        return plazo;
    }

    public void setPlazo(String plazo) {
        this.plazo = plazo;
    }

    public String getPorcentaje() {
        return porcentaje;
    }

    public void setPorcentaje(String porcentaje) {
        this.porcentaje = porcentaje;
    }

    public String getModalidad() {
        return modalidad;
    }

    public void setModalidad(String modalidad) {
        this.modalidad = modalidad;
    }

    public String getValormin() {
        return valormin;
    }

    public void setValormin(String valormin) {
        this.valormin = valormin;
    }

    public String getValormax() {
        return valormax;
    }

    public void setValormax(String valormax) {
        this.valormax = valormax;
    }

}